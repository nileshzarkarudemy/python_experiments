# Addition = it prints the sum
print(3 + 5)

# Substraction = it prints the difference
print(5 - 3)

# Multiplication = it prints the product
print(2 * 5)

# Division = it prints the quotient
print(10 / 2)

# Modulus = it prints the remainder 
print(10 % 3)

# Exponentiation = it prints the result of raising a number to a power
print(2 ** 3)

