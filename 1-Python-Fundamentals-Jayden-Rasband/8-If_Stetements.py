# if statements
age = 18
if age >= 18:
    print("You are an adult.")
else:
    print("You are a kid.")

name = "John"
if name == "John":
    print("Hello John")
else:
    print("Hello stranger")



# if else if
height = 170
if height >= 180:
    print("You are tall.")
elif height >= 160:
    print("You are average.")
else:
    print("You are short.")

# if elif else
grade = 75
if grade >= 90:
    print("A")
elif grade >= 80:
    print("B")
elif grade >= 70:
    print("C")
elif grade >= 60:
    print("D")
else:
    print("F")

