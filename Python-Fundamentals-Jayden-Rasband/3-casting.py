# string to integer
score = "10"
print(type(score))
print(score)
# type casting
int_score = int(score)
print(type(int_score))
print(int_score)


# string to float
price = "9.99"
print(type(price))
print(price)
# type casting
float_price = float(price)
print(type(float_price))
print(float_price)



