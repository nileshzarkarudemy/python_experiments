
# Comparison Operators

# Equal to
print("3 == 3 : ",3 == 3)
print("3 == 4 : ",3 == 4)

# Not equal to
print("3 != 4 : ",3 != 4)
print("3 != 3 : ",3 != 3)

# Greater than
print("4 > 3 : ",4 > 3)
print("3 > 4 : ",3 > 4)

# Less than
print("3 < 4 : ",3 < 4)
print("4 < 3 : ",4 < 3)

# Greater than or equal to
print("3 >= 3 : ",3 >= 3)
print("4 >= 3 : ",4 >= 3)
print("3 >= 4 : ",3 >= 4)

# Less than or equal to
print("3 <= 3 : ",3 <= 3)
print("3 <= 4 : ",3 <= 4)
print("4 <= 3 : ",4 <= 3)

